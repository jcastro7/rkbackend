package pe.com.empresa.rk.view.model;

public class RegistroMarcacionEmpleadoViewModel {

	private String CodigoEmpleado;
	private String DniEmpleado;
	private String FechaMarcacion;
	private String HoraMarcacion;
	private String TipoMarcacion;
	
	public String getCodigoEmpleado() {
		return CodigoEmpleado;
	}
	public void setCodigoEmpleado(String codigoEmpleado) {
		CodigoEmpleado = codigoEmpleado;
	}
	public String getDniEmpleado() {
		return DniEmpleado;
	}
	public void setDniEmpleado(String dniEmpleado) {
		DniEmpleado = dniEmpleado;
	}
	public String getFechaMarcacion() {
		return FechaMarcacion;
	}
	public void setFechaMarcacion(String fechaMarcacion) {
		FechaMarcacion = fechaMarcacion;
	}
	public String getHoraMarcacion() {
		return HoraMarcacion;
	}
	public void setHoraMarcacion(String horaMarcacion) {
		HoraMarcacion = horaMarcacion;
	}
	public String getTipoMarcacion() {
		return TipoMarcacion;
	}
	public void setTipoMarcacion(String tipoMarcacion) {
		TipoMarcacion = tipoMarcacion;
	}
	
}
